// builds HTML cards for each person given relevant strings
function createCard(name, role, classYear, about, email, linkedin, imageName) {
    const template = document.createElement("template");
    // determines if linkedin tag should be added if provided 
    const linkedinHTML = linkedin ? `
        <a class="linkedin" target="_blank" href="${linkedin}">
            <img src="images/assets/linkedin.png" height="30">
        </a>
    ` : '';
    template.innerHTML = `
    <div class="col-xl-3 col-md-6 mb-4">
        <div class="flip-card">
            <div class="flip-card-inner">
                <div class="flip-card-front">
                    <div class="image-overlay" style="border: 4px solid #000; border-radius: 23px; overflow: hidden;">
                        <img src="./images/headshots/${imageName}" class="card-img-top person-image">
                        <div class="overlay-text">
                            <h5 class="underlined">${name}</h5>
                            <p>${role}, ${classYear}</p>
                        </div>
                    </div>
                </div>
                <div class="flip-card-back">
                    <h5 class="underlined" style="margin: 0px;">${name}</h5>
                    <p style="margin: 0px;">${role}, ${classYear}</p>
                    <p>${about}</p>
                    <span class="person-footer">
                        <a class="mail" href=${email}>
                            <img src="images/assets/mail.svg" width="30" height="30" onmouseover="hoverMail(this);" onmouseout="unhoverMail(this);">
                        </a>
                        ${linkedinHTML}
                    </span>
                </div>
            </div>
        </div>
    </div>
      `;
    return template.content.firstElementChild;
}

// builds an empty row for the cards to be placed in
function createRow() {
    const template = document.createElement("template");
    template.innerHTML = `
        <div class="row justify-content-center">

        </div>
      `;
    return template.content.firstElementChild;
}

/*
    Builds rows of 3 cards for each person in the given JSON
    @param currentRow: the row to be added to (HTMLElement)
    @param cardData: the JSON of people to be added
*/
function generateCards(currentRow, cardData) {

    let tempRow = createRow();

    for (let i = 0; i < cardData.length; i++) {
        const card = createCard(cardData[i].name, cardData[i].role, cardData[i].classYear, cardData[i].about, cardData[i].email, cardData[i].linkedin, cardData[i].imageName);

        if ((i + 1) % 3 == 0) {
            // Create a new row every 3 cards
            tempRow.appendChild(card);
            currentRow.appendChild(tempRow);
            tempRow = createRow();
        } else {
            // Add the card to the current row
            tempRow.appendChild(card);
        }
        // leadershipRow.appendChild(card);
    }
    if (cardData.length % 3 != 0) {
        currentRow.appendChild(tempRow);
    }
}

